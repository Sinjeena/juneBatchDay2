package operatorsExample;

public class ArithmenticOperatorDemo {

	public static void main(String[] args) {
		// +Add    -Subtract    *Multiply      /Division   %Modulus
		 
		int num1=34;
		int num2=24;
		
		//ADD these numbers
		int sum=num1+num2;       //30+20=50
		System.out.println("Sum of num1 and num2 ="+sum); //50
		System.out.println("--------------------");
		
		//Subtract operation
		int diff=num1-num2;  
		System.out.println("Difference of num1 and num2="+diff);
		System.out.println("--------------------");
		
		//Multiplication operation
		int multiple= num1*num2;
		System.out.println("Multiplication result of num1 and num2="+multiple);
		System.out.println("--------------------");
		
		//Division operation
		int division= num1/num2;
		System.out.println("The division of num1 and num2 is="+division);
		System.out.println("--------------------");
		
		//Modulus operation
		int result= num1%num2;
		System.out.println("The Modulus of num1 and num2="+result);
		System.out.println("--------------------");
	
	
	
	
	
	
	
	
	
	}
	

}
